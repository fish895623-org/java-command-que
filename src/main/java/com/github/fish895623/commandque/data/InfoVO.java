package com.github.fish895623.commandque.data;

public class InfoVO {
  private String id;
  private String name;
  private String t;

  public String getId() {
    return this.id;
  }

  public void setId(String val) {
    this.id = val;
  }

  public String getName() {
    return this.name;
  }

  public void setName(String val) {
    this.name = val;
  }

  public void setT(String val) {
    this.t = val;
  }

  public String getT() {
    return this.t;
  }
}
